package chapter_03;

public class _04_EscapeSequence {
    public static void main(String[] args) {
        // 특수문자
        // \n(줄바꿈), \t(탭키), \\(\), \"("), \'(')
        System.out.println("I like 'java'"); // ""(큰 따옴표) 안에 '(작은 따옴표)사용해서 표시 가능

        char c = '\'';
        System.out.println(c);
    }
}
